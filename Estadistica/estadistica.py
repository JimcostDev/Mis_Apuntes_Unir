import numpy as np

# Datos de ejemplo
datos = [10, 28, 40, 46]

# Calcular la media
media = np.mean(datos)

# Calcular la varianza
varianza = np.var(datos)

# Calcular la desviación estándar
desviacion_tipica = np.std(datos)

print("Media:", media)
print("Varianza:", varianza)
print("Desviación Típica:", desviacion_tipica)
